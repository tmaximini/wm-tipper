/**
 * POST /signup
 * Create a new local account.
 * @param email
 * @param password
 */

exports.postSignup = function(req, res, next) {
  req.assert('email', 'Ungültige Email').isEmail();
  req.assert('password', 'Das Passwort muss aus mindestens 6 Zeichen bestehen').len(6);
  req.assert('confirmPassword', 'Die Passwörter stimmen nicht überein').equals(req.body.password);

  var errors = req.validationErrors();

  if (!req.body.profile.name) {
    errors.push('Bitte gib einen namen ein.');
  }

  if (errors) {
    req.flash('error', errors);
    return res.redirect('/signup');
  }

  var user = new User({
    email: req.body.email,
    password: req.body.password
  });

  console.dir(req.body);

  user.profile.name = req.body.profile.name;

  user.save(function(err) {
    if (err) {
      console.dir(err);
      if (err.code === 11000) {
        req.flash('error', { msg: 'Es existiert bereits ein Benutzer mit dieser Email.' });
      } else {
        req.flash('error', { msg: err });
      }
      return res.redirect('/signup');
    }
    req.logIn(user, function(err) {
      if (err) return next(err);
      res.redirect('/erste-schritte');
    });
  });
};

